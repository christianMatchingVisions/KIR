var RLAAF={
	config:{
		site_path: null,
		api: '/wp-json/rlaaf/search/run',
		render_template: null,
		render_container: null,
		pagination_mode: 'append',
		items_per_page: 10
	},

	current_settings:[],
	data:[],
	
	pagination: {
		items_per_page: 10,
		page:1
	},	

	setup: function(config){
		this.config=Object.assign(this.config, config)
		this.pagination.items_per_page=this.config.items_per_page;
	},

	resetPagination: function(){
		this.pagination={
			items_per_page: this.config.items_per_page||10,
			page: 1
		};

		return this;
	},

	addSettings: function(settings){
		this.current_settings=settings;
		return this;
	},

	prevPage: function(){
		this.pagination.page-=1;
		return this.loadItems();
	},

	nextPage: function(){
		this.pagination.page+=1;
		return this.loadItems();
	},

	loadItems:function(){
		/*
		STATIC-BUILD PATCH: the original implementation POSTed
		{settings+pagination} to {site_path}/wp-json/rlaaf/search/run.
		That endpoint doesn't exist on the static Astro build, so we GET
		pre-captured responses from /rlaaf-data/<settings-name>/page-<N>.json
		(generated by scripts/capture-rlaaf.mjs). main.js records the
		settings file URL on RLAAF.settings_source.
		*/
		var name=String(this.settings_source||'').trim().split('/').pop().replace(/\.json$/i,'');
		if(name.length===0){
			throw new Error("Unable to load items: no settings source recorded.");
		}

		return $.get('/rlaaf-data/'+name+'/page-'+this.pagination.page+'.json');
	},

	render: function(data){
		/*
		Run any pre-render function.
		*/
		if(this.prerender){
			data=this.prerender(data);
		}

		/* Add index to data */
		for(var i=0; i<data.data.length; i++){
			data.data[i].index=i+1+(this.pagination.page*this.pagination.items_per_page-this.pagination.items_per_page); 
		}

		var content=Mustache.render(this.config.render_template,data);

		if(this.pagination.page===1||this.pagination_mode==='replace'){
			this.config.render_container.html(content);
		}
		else{
			this.config.render_container.append(content);
		}
	},

	//bind where to render
	renderError: function(){
		/* STATIC-BUILD PATCH: was a blocking alert(); fail silently so an
		   uncaptured filter combination degrades gracefully. */
		if(window.console&&console.warn){
			console.warn('RLAAF: no pre-captured data for this search (static build).');
		}
	}
};